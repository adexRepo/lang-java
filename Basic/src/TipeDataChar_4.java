public class TipeDataChar_4 {
    //Tipe Data Character
    /* 
        1. Data Character (huruf) di java direpresentasikan oleh tipe char
        2. Untuk membuat data char di Java, kita bisa menggunakan tanda ' (petik satu)
            di awal dan akhir karakter
    */
    public static void main(String[] args){
        char a = 'A';
        char d = 'D';
        char x = 'X';
        System.out.print(a);
        System.out.print(d);
        System.out.print(x);
    }

}
