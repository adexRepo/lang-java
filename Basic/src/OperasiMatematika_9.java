public class OperasiMatematika_9 {
    public static void main(String[] args){
        //Operasi Matematika di Javva
        /* 
            - Penjumlahan +
            - Pengurangan -
            - Perkalian *
            - Pembagian /
            - Modulus %
        */
        int a = 10;
        int b = 5;
        int c = a + b;
        System.out.println("Hasil Penjumlahan : " + c);
        c = a - b;
        System.out.println("Hasil Pengurangan : " + c);
        c = a * b;
        System.out.println("Hasil Perkalian : " + c);
        c = a / b;
        System.out.println("Hasil Pembagian : " + c);
        c = a % b;
        System.out.println("Hasil Modulus : " + c);

    }
}
