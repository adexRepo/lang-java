public class WhileLoop_21 {
    public static void main(String[] args) {
        // While Loop
        /* 
            1. While loop adalah veri perulangan yang lebih sederhana dibandingkan for loop.
            2. Di While loop, hanya terdapat kondisi perulangan, tanpa ada init statement dan post statement.
        */

        int i = 0;
        while (i < 10) { // kondisi perulangan
            System.out.println("Perualangan While ke = " + i);
            i++;
        }
    }
}
