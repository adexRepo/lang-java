public class TipeDataBoolean_5 {
    public static void main(String[] args){
        //Tipe Data Boolean
        /* 
            1. Tipe data boolean adalah tipe data yang memiliki 2 nilai, yaitu true dan false
            2. Tipe data boolean di java direpresentasikan dengan kata boolean
            3. Nilai benar direpresentasikan dengan kata true
            4. Nilai salah direpresentasikan dengan kata false
            5. Default value untuk boolean adalah false
        */
        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);
    }
}
