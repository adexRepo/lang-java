public class OperasiPerbandingan_12 {
    public static void main(String[] args){
        //Operasi Perbandingan
        /* 
            1. Operasi perbandingan adalah operasi untuk membandingkan dua buah data
            2. Operasi Perbandingan adalah operasi yang menghasilkan nilai boolean (true or false)
            3. Jika hasil benar maka nilainya true
            4. Jika hasil salah maka nilainya false

            //Contoh2nya
            - >    --> lebih dari
            - <    --> kurang dari
            - >=   --> lebih dari sama dengan
            - <=   --> kurang dari sama dengan
            - ==   --> sama dengan
            - !=   --> tidak sama dengan
        */
        int a = 10;
        int b = 20;

        System.out.println(a > b); //false
        System.out.println(a < b); //true
        System.out.println(a >= b); //false
        System.out.println(a <= b); //true
        System.out.println(a == b); //false
        System.out.println(a != b); //true
    }
}
